package com.fetch;

public class ConfigProps {
	private String xmlTarget;
	private String urlsFile;
	private String username;
	private String password;
	private String proxyHost;
	private String proxyPort;
	public String getXmlTarget() {
		return xmlTarget;
	}
	public void setXmlTarget(String xmlTarget) {
		this.xmlTarget = xmlTarget;
	}
	public String getUrlsFile() {
		return urlsFile;
	}
	public void setUrlsFile(String urlsFile) {
		this.urlsFile = urlsFile;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProxyHost() {
		return proxyHost;
	}
	public void setProxyHost(String proxyHost) {
		this.proxyHost = proxyHost;
	}
	public String getProxyPort() {
		return proxyPort;
	}
	public void setProxyPort(String proxyPort) {
		this.proxyPort = proxyPort;
	}
	

}

